<?php
function sem_NumToWord($sem)
{
	if($sem==3)
	{
		return("THIRD SEMESTER");
	}
	if($sem==4)
	{
		return("FOURTH SEMESTER");
	}
	if($sem==5)
	{
		return("FIFTH SEMESTER");
	}
	if($sem==6)
	{
		return("SIXTH SEMESTER");
	}
	if($sem==7)
	{
		return("SEVENTH SEMESTER");
	}
	if($sem==8)
	{
		return("EIGHTH SEMESTER");
	}
}
?>